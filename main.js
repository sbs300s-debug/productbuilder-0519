let currentLang = localStorage.getItem('siteLang') || 'ko';

function updateUI() {
    const t = translations[currentLang];
    document.title = t.siteTitle;
    document.getElementById('header-title').textContent = t.headerTitle;
    document.getElementById('hero-msg').textContent = t.heroMsg;
    document.getElementById('hero-submsg').textContent = t.heroSubmsg;
    document.getElementById('result-title').textContent = t.resultTitle;
    document.getElementById('comment-title').textContent = t.commentTitle;
    document.getElementById('footer-text').textContent = t.footerText;
    
    const langSelect = document.getElementById('lang-select');
    if (langSelect) langSelect.value = currentLang;

    const aboutTitleElements = document.querySelectorAll('.about-title-text');
    aboutTitleElements.forEach(el => el.textContent = t.aboutTitle);
    
    const aboutContentElements = document.querySelectorAll('.about-content-text');
    aboutContentElements.forEach(el => el.textContent = t.aboutContent);
    document.getElementById('reaction-msg').textContent = t.reactionMsg;
    document.getElementById('user-nickname').placeholder = t.nicknamePlaceholder;

    const privacyLinkText = document.getElementById('privacy-link-text');
    if (privacyLinkText) privacyLinkText.textContent = t.privacyTitle;
    
    const homeLinkText = document.getElementById('home-link-text');
    if (homeLinkText) homeLinkText.textContent = currentLang === 'ko' ? "홈" : (currentLang === 'ja' ? "ホーム" : (currentLang === 'es' ? "Inicio" : (currentLang === 'zh' ? "首页" : "Home")));

    document.getElementById('contact-title').textContent = t.contactTitle;
    document.getElementById('label-email').textContent = t.labelEmail;
    document.getElementById('label-message').textContent = t.labelMessage;
    document.getElementById('submit-btn').textContent = t.submitBtn;
    document.getElementById('email').placeholder = t.placeholderEmail;
    document.getElementById('message').placeholder = t.placeholderMessage;

    const moodButtonsContainer = document.getElementById('mood-buttons');
    if (moodButtonsContainer) {
        moodButtonsContainer.innerHTML = '';
        Object.keys(t.moods).forEach(mood => {
            const btn = document.createElement('button');
            btn.className = 'mood-btn';
            btn.textContent = t.moods[mood];
            btn.onclick = () => showRecommendations(mood);
            moodButtonsContainer.appendChild(btn);
        });
    }

    const lastMood = localStorage.getItem('lastMood');
    if (lastMood) showRecommendations(lastMood);
}

function showRecommendations(mood) {
    localStorage.setItem('lastMood', mood);
    const t = translations[currentLang];
    
    document.querySelectorAll('.mood-btn').forEach(btn => {
        btn.classList.toggle('active', btn.textContent === t.moods[mood]);
    });

    const area = document.getElementById('recommendation-area');
    if (!area) return;
    
    const grid = document.getElementById('movie-grid');
    grid.innerHTML = '';

    const movies = movieData[mood] || [];
    const shuffledMovies = [...movies].sort(() => Math.random() - 0.5);

    shuffledMovies.forEach(movie => {
        const card = document.createElement('div');
        card.className = 'movie-card';
        const title = movie.title[currentLang] || movie.title['en'];
        const desc = movie.desc[currentLang] || movie.desc['en'];
        
        card.innerHTML = `
            <img src="${movie.img}" alt="${title}" class="movie-poster" loading="lazy">
            <div class="movie-info">
                <span class="movie-meta">${t.year}: ${movie.year} | ${t.director}: ${movie.director}</span>
                <h3>${title}</h3>
                <p>${desc}</p>
            </div>
        `;
        grid.appendChild(card);
    });

    area.classList.add('visible');
    area.scrollIntoView({ behavior: 'smooth' });
}

// Mood Reactions Logic
const reactionButtons = document.querySelectorAll('.reaction-btn');

function initReactions() {
    const reactionList = document.getElementById('reaction-list');
    const nicknameInput = document.getElementById('user-nickname');
    
    // Load saved nickname
    const savedName = localStorage.getItem('userNickname');
    if (savedName) nicknameInput.value = savedName;

    // Add some initial mock reactions if list is empty
    if (reactionList.children.length === 0) {
        const mockNames = ["MovieLover77", "CinemaStar", "무비마스터", "PopcornFan"];
        const emojis = ["🥰", "🤣", "😮", "👍"];
        for(let i=0; i<3; i++) {
            addReactionToFeed(mockNames[i], emojis[i]);
        }
    }

    reactionButtons.forEach(btn => {
        const mood = btn.dataset.mood;
        const countId = `count-${mood}`;
        const emoji = btn.querySelector('.emoji').textContent;
        
        let count = parseInt(localStorage.getItem(`reaction-${mood}`));
        if (isNaN(count)) {
            count = Math.floor(Math.random() * 50) + 10;
            localStorage.setItem(`reaction-${mood}`, count);
        }
        
        document.getElementById(countId).textContent = count;
        
        btn.onclick = function() {
            if (btn.classList.contains('clicked')) return;
            
            const nickname = nicknameInput.value.trim() || translations[currentLang].anonymous;
            localStorage.setItem('userNickname', nicknameInput.value); // Save current input
            
            btn.classList.add('clicked');
            count++;
            document.getElementById(countId).textContent = count;
            localStorage.setItem(`reaction-${mood}`, count);
            
            addReactionToFeed(nickname, emoji);
        };
    });
}

function addReactionToFeed(name, emoji) {
    const reactionList = document.getElementById('reaction-list');
    const t = translations[currentLang];
    
    const li = document.createElement('li');
    li.className = 'reaction-item';
    li.innerHTML = `
        <span><span class="user-id">${name}</span>${t.reactionText}</span>
        <span class="emoji">${emoji}</span>
    `;
    
    reactionList.appendChild(li);
    
    // Keep only last 10
    if (reactionList.children.length > 10) {
        reactionList.removeChild(reactionList.firstChild);
    }

    // Scroll to bottom
    const feed = document.querySelector('.reaction-feed');
    feed.scrollTop = feed.scrollHeight;
}

const langSelect = document.getElementById('lang-select');
if (langSelect) {
    langSelect.addEventListener('change', (e) => {
        currentLang = e.target.value;
        localStorage.setItem('siteLang', currentLang);
        updateUI();
    });
}

document.getElementById('theme-toggle').addEventListener('click', () => {
    document.body.classList.toggle('dark-mode');
    const isDark = document.body.classList.contains('dark-mode');
    document.getElementById('theme-toggle').textContent = isDark ? translations[currentLang].themeLight : translations[currentLang].themeDark;
});

const backToTopBtn = document.getElementById("back-to-top");
if (backToTopBtn) {
    window.onscroll = function() {
        if (document.body.scrollTop > 300 || document.documentElement.scrollTop > 300) {
            backToTopBtn.style.display = "block";
        } else {
            backToTopBtn.style.display = "none";
        }
    };
    backToTopBtn.onclick = function() {
        window.scrollTo({ top: 0, behavior: 'smooth' });
    };
}

